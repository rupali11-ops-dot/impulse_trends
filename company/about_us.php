  if(($(this).val()).length!=10)
  {
    $(".mob_group .temp-error").addClass("display_none");
   $(".mob_group .temp-error_10").removeClass("display_none");
  }
  
  if(($(this).val()).length==10)
  {
    $(this).closest(".form-group").find(".temp-error_10").addClass("display_none"); 
  }

});

$(".email").keyup(function(){
  $(this).closest(".form-group").find(".temp-error").addClass("display_none");

  // var reg = /^([A-Za-z0-9_\-\.])+\@([A-Za-z0-9_\-\.])+\.([A-Za-z]{2,4})$/;

   if (reg.test($(".email").val()) == false) 
    {
        $(".email_group .temp-error_invalid").removeClass("display_none");
       ;
        return false;
    }
     $(".email_group .temp-error_invalid").addClass("display_none");
     return true;

});

