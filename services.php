<script type="text/javascript">

   var reg = /^([A-Za-z0-9_\-\.])+\@([A-Za-z0-9_\-\.])+\.([A-Za-z]{2,4})$/;

$(".full_name").keyup(function(){
  $(this).closest(".form-group").find(".temp-error").addClass("display_none");
});

$(".mob").keyup(function(){
  $(this).closest(".form-group").find(".temp-error").addClass("display_none");
  
  if(($(this).val()).length!=10)
  {
    $(".mob_group .temp-error").addClass("display_none");
   $(".mob_group .temp-error_10").removeClass("display_none");
  }
  
  if(($(this).val()).length==10)
  {
    $(this).closest(".form-group").find(".temp-error_10").addClass("display_none"); 
  }

});

$(".email").keyup(function(){
  $(this).closest(".form-group").find(".temp-error").addClass("display_none");

  // var reg = /^([A-Za-z0-9_\-\.])+\@([A-Za-z0-9_\-\.])+\.([A-Za-z]{2,4})$/;

   if (reg.test($(".email").val()) == false) 
    {
        $(".email_group .temp-error_invalid").removeClass("display_none");
       ;
        return false;
    }
     $(".email_group .temp-error_invalid").addClass("display_none");
     return true;

});

$(".area").keyup(function(){
  $(this).closest(".form-group").find(".temp-error").addClass("display_none");
});

$(".msg").keyup(function(){
  $(this).closest(".form-group").find(".temp-error").addClass("display_none");
});

$(document).on('click','.date_time',function(){
  $(this).closest(".form-group").find(".temp-error").addClass("display_none");
});

function submit_form()
  {
    var name=$(".full_name").val();   
    var area=$(".area").val();   
    var mob=$(".mob").val();   
    var msg=$(".msg").val();  
    var email=$(".email").val();
    var date_time=$(".date_time").val();

    if(name!="" && area!="" && mob!="" && msg!="" && email!="" && date_time!="")
    {
      if(mob!="" && mob.length==10)
        {
          if(email!="" && reg.test($(".email").val()) == true)
            {
               $(".download_loader").removeClass("display_none");
                $(".contact_btn").attr( "disabled", true);
                  $.ajax({
                        type    : "POST",
                        url     : "<?= $dir; ?>serviceContactus.php",
                        data    : {name:name,area:area,mob:mob,msg:msg,email:email,date_time:date_time},
                        success: function(data)
                        {
                           $(".download_loader").addClass("display_none");
                            $(".contact_btn").removeAttr("disabled");
                            $("#contact_us_form")[0].reset();
                        }
                      });
            }
        }
       
      }
    else
    {
         if(name=="")
         {
          $(".name_group").find(".temp-error").removeClass("display_none");
         }
         if(email=="")
         {
          $(".email_group").find(".temp-error").removeClass("display_none");
         }
         if(area=="")
         {
          $(".area_group").find(".temp-error").removeClass("display_none");
         }
         if(msg=="")
         {
          $(".msg_group").find(".temp-error").removeClass("display_none");
         }
         if(date_time=="")
         {
          $(".date_group").find(".temp-error").removeClass("display_none");
         }
         if(mob=="")
         {
          $(".mob_group").find(".temp-error").removeClass("display_none");
         }
     }
 }
</script>

