function submit_form()
  {
    var name=$(".full_name").val();   
    var area=$(".area").val();   
    var mob=$(".mob").val();   
    var msg=$(".msg").val();  
    var email=$(".email").val();
    var date_time=$(".date_time").val();

    if(name!="" && area!="" && mob!="" && msg!="" && email!="" && date_time!="")
    {
      if(mob!="" && mob.length==10)
        {
          if(email!="" && reg.test($(".email").val()) == true)
            {
               $(".download_loader").removeClass("display_none");
                $(".contact_btn").attr( "disabled", true);
                  $.ajax({
                        type    : "POST",
                        url     : "<?= $dir; ?>sendContactus.php",
                        data    : {name:name,area:area,mob:mob,msg:msg,email:email,date_time:date_time},
                        success: function(data)
                        {
                           $(".download_loader").addClass("display_none");
                            $(".contact_btn").removeAttr("disabled");
                            $("#contact_us_form")[0].reset();
                        }
                      });
            }
        }
       
      }
    else
    {
         if(name=="")
         {
          $(".name_group").find(".temp-error").removeClass("display_none");
         }
         if(email=="")
         {
          $(".email_group").find(".temp-error").removeClass("display_none");
         }
         if(area=="")
         {
          $(".area_group").find(".temp-error").removeClass("display_none");
         }
         if(msg=="")
         {
          $(".msg_group").find(".temp-error").removeClass("display_none");
         }
         if(date_time=="")
         {
          $(".date_group").find(".temp-error").removeClass("display_none");
         }
         if(mob=="")
         {
          $(".mob_group").find(".temp-error").removeClass("display_none");
         }
     }
 }
</script>
