<!-- download form validations -->

<script type="text/javascript">
  var reg = /^([A-Za-z0-9_\-\.])+\@([A-Za-z0-9_\-\.])+\.([A-Za-z]{2,4})$/;

$(".ak_download_modal .download_name").keyup(function(){
  $(this).closest(".form-group").find(".temp-error").addClass("display_none");
});

$(".ak_download_modal .download_no").keyup(function(){
  $(this).closest(".form-group").find(".temp-error").addClass("display_none");
  
  if(($(this).val()).length!=10)
  {
    $(".ak_download_modal .download_mob_group .temp-error").addClass("display_none");
   $(".ak_download_modal .download_mob_group  .temp-error_10").removeClass("display_none");
  }
  
  if(($(this).val()).length==10)
  {
    $(this).closest(".form-group").find(".temp-error_10").addClass("display_none"); 
  }

});

$(".ak_download_modal .download_email").keyup(function(){
  $(this).closest(".form-group").find(".temp-error").addClass("display_none");

  // var reg = /^([A-Za-z0-9_\-\.])+\@([A-Za-z0-9_\-\.])+\.([A-Za-z]{2,4})$/;

   if (reg.test($(".ak_download_modal .download_email").val()) == false) 
    {
        $(".ak_download_modal .download_email_group .temp-error_invalid").removeClass("display_none");
       ;
        return false;
    }
     $(".ak_download_modal .download_email_group .temp-error_invalid").addClass("display_none");
     return true;

});




  $("#ak_danum_dwn-btn").click(function(){


    var ak_danum_d_name=$(".ak_download_modal .download_name").val();
    var ak_danum_d_email=$(".ak_download_modal .download_email").val();
    var ak_danum_d_mob=$(".ak_download_modal .download_no").val();


    if(ak_danum_d_name!="" && ak_danum_d_email!="" && ak_danum_d_mob!="" && reg.test($(".ak_download_modal .download_email").val()) == true && ak_danum_d_mob.length==10)
    {

    $(".ak_download_modal .download_loader").removeClass("display_none");
    $("#ak_danum_dwn-btn").attr( "disabled", true);

          $.ajax({
                    type    : "POST",
                    url     : "ak-danum_download_file.php",
                    data    : { ak_danum_d_name:ak_danum_d_name,ak_danum_d_email:ak_danum_d_email,ak_danum_d_mob:ak_danum_d_mob},
                    success: function(data)
                    {
                      // download file
                       $.ajax({
                            url: 'AK-Danum-Bathroom-Accessories-Catalogue.pdf',
                            method: 'GET',
                            xhrFields: {
                                responseType: 'blob'
                            },
                            success: function (data) {

                               $(".ak_download_modal .download_loader").addClass("display_none");
                               $("#ak_danum_dwn-btn").removeAttr("disabled");
                               $('#ak_danum_basicModal').modal('hide');

                                var a = document.createElement('a');
                                var url = window.URL.createObjectURL(data);
                                a.href = url;
                                a.download = 'AK-Danum-Bathroom-Accessories-Catalogue.pdf';
                                document.body.append(a);
                                a.click();
                                a.remove();
                                window.URL.revokeObjectURL(url);
                            }
                        });

                    }
            });

        
    }
    else{

       if(ak_danum_d_name=="")
         {
          $(".ak_download_modal .download_name_group").find(".temp-error").removeClass("display_none");
         }
         if(ak_danum_d_email=="")
         {
          $(".ak_download_modal .download_email_group").find(".temp-error").removeClass("display_none");
         }
         if(ak_danum_d_mob=="")
         {
          $(".ak_download_modal .download_mob_group").find(".temp-error").removeClass("display_none");
         }

    }



  });

$('#ak_danum_basicModal .close').on('click', function (e) {
 $(".ak_download_modal .temp-error,.ak_download_modal .temp-error_10,.ak_download_modal .temp-error_invalid,.ak_download_modal .temp-error_num").addClass('display_none');
  document.getElementById("ak_download_form").reset();
});


</script>
